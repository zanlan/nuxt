            （三）制定合理方案，避免盲目投资。老年投资者应首先规划好可用的资金，留足日常生活所需的流动现金后，结合自身的财力、精力和期望生活标准等情况再将剩下的资产制定适合的理财方案，切忌盲目投资。
          </p>
