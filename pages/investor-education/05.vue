            （一）防范投资渠道风险。由于现在很多老年人接触的市场信息较少，没有专业的理财技能，容易被一些非法证券期货经营机构打的幌子所吸引，进而遭受巨大的财产损失。老年投资者可通过中国证监会、中国证券业协会、中国期货业协会网站查询合法证券期货经营机构名单。
          </p>
